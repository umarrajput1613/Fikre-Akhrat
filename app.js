let currentHadith = 0;

/* =========================
   LOAD HADITH FROM URL
========================= */

const params =
new URLSearchParams(window.location.search);

const hadithParam =
params.get("hadith");

if(hadithParam !== null){

    currentHadith =
    parseInt(hadithParam);

    if(
        isNaN(currentHadith) ||
        currentHadith < 0 ||
        currentHadith >= hadiths.length
    ){

        currentHadith = 0;

    }

}

/* =========================
   ELEMENTS
========================= */

const arabicText =
document.getElementById("arabicText");

const urduText =
document.getElementById("urduText");

const referenceText =
document.getElementById("referenceText");

const hadithCount =
document.getElementById("hadithCount");

/* =========================
   UPDATE URL
========================= */

function updateURL(){

    const newURL =
    `${window.location.pathname}?hadith=${currentHadith}`;

    window.history.replaceState(
        null,
        null,
        newURL
    );

}

/* =========================
   GET CURRENT URL
========================= */

function getCurrentURL(){

    return window.location.href;

}

/* =========================
   RENDER FUNCTION
========================= */

function renderHadith(){

    arabicText.innerHTML =
    hadiths[currentHadith].arabic;

    urduText.innerHTML =
    hadiths[currentHadith].urdu;

    referenceText.innerHTML =
    hadiths[currentHadith].ref;

    hadithCount.innerHTML =
    `حدیث ${currentHadith + 1} / ${hadiths.length}`;

    updateURL();

}

/* =========================
   NEXT
========================= */

function nextHadith(){

    currentHadith++;

    if(currentHadith >= hadiths.length){

        currentHadith = 0;

    }

    renderHadith();

}

/* =========================
   PREVIOUS
========================= */

function prevHadith(){

    currentHadith--;

    if(currentHadith < 0){

        currentHadith =
        hadiths.length - 1;

    }

    renderHadith();

}

/* =========================
   AUTO SLIDE
========================= */

setInterval(()=>{

    nextHadith();

},8000);

/* =========================
   FIRST LOAD
========================= */

renderHadith();

/* =========================
   PDF DOWNLOAD
========================= */

async function downloadPDF(){

    const card =
    document.querySelector(".slider-card");

    const canvas =
    await html2canvas(card,{

        scale:4,
        useCORS:true,
        backgroundColor:"#120d09"

    });

    const imgData =
    canvas.toDataURL("image/png");

    const pdf =
    new jspdf.jsPDF({

        orientation:"portrait",
        unit:"mm",
        format:"a4"

    });

    const pdfWidth = 210;

    const pdfHeight =
    (canvas.height * pdfWidth)
    / canvas.width;

    pdf.addImage(
        imgData,
        "PNG",
        0,
        0,
        pdfWidth,
        pdfHeight
    );

    pdf.save(
        `Hadith-${currentHadith + 1}.pdf`
    );

}

/* =========================
   COPY HADITH
========================= */

function copyHadith(){

    const shareURL =
    getCurrentURL();

    const text =

    "چہل احادیث مبارکہ\n\n" +

    hadiths[currentHadith].arabic +

    "\n\n" +

    hadiths[currentHadith].urdu +

    "\n\n" +

    hadiths[currentHadith].ref +

    "\n\n" +

    shareURL;

    navigator.clipboard.writeText(text);

    alert("حدیث کاپی ہوگئی");

}

